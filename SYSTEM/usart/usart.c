//初始化IO 串口1 
//bound:波特率
void uart_init(u32 bound){
    //GPIO端口设置
    GPIO_InitTypeDef GPIO_InitStructure;
	USART_InitTypeDef USART_InitStructure;
	NVIC_InitTypeDef NVIC_InitStructure;
	 
	RCC_APB2PeriphClockCmd(RCC_APB2Periph_USART1|RCC_APB2Periph_GPIOA, ENABLE);	//使能USART1，GPIOA时钟
 	USART_DeInit(USART1);  //复位串口1
	//USART1_TX   PA.9
    GPIO_InitStructure.GPIO_Pin = GPIO_Pin_9; //PA.9
    GPIO_InitStructure.GPIO_Speed = GPIO_Speed_50MHz;
    GPIO_InitStructure.GPIO_Mode = GPIO_Mode_AF_PP;	//复用推挽输出
    GPIO_Init(GPIOA, &GPIO_InitStructure); //初始化PA9
   
    //USART1_RX	  PA.10
    GPIO_InitStructure.GPIO_Pin = GPIO_Pin_10;
    GPIO_InitStructure.GPIO_Mode = GPIO_Mode_IN_FLOATING;//浮空输入
    GPIO_Init(GPIOA, &GPIO_InitStructure);  //初始化PA10

   //Usart1 NVIC 配置

    NVIC_InitStructure.NVIC_IRQChannel = USART1_IRQn;
	NVIC_InitStructure.NVIC_IRQChannelPreemptionPriority=3 ;//抢占优先级3
	NVIC_InitStructure.NVIC_IRQChannelSubPriority = 3;		//子优先级3
	NVIC_InitStructure.NVIC_IRQChannelCmd = ENABLE;			//IRQ通道使能
	NVIC_Init(&NVIC_InitStructure);	//根据指定的参数初始化VIC寄存器
  
   //USART 初始化设置
	USART_InitStructure.USART_BaudRate = bound;//一般设置为9600;
	USART_InitStructure.USART_WordLength = USART_WordLength_8b;//字长为8位数据格式
	USART_InitStructure.USART_StopBits = USART_StopBits_1;//一个停止位
	USART_InitStructure.USART_Parity = USART_Parity_No;//无奇偶校验位
	USART_InitStructure.USART_HardwareFlowControl = USART_HardwareFlowControl_None;//无硬件数据流控制
	USART_InitStructure.USART_Mode = USART_Mode_Rx | USART_Mode_Tx;	//收发模式

    USART_Init(USART1, &USART_InitStructure); //初始化串口
    USART_ITConfig(USART1, USART_IT_RXNE, ENABLE);//开启中断
    USART_Cmd(USART1, ENABLE);                    //使能串口 

}

u8 transFlag = TRANS_NONE;
u8 usartRecieveCounter = 0;
//重写一下串口中断
void USART1_IRQHandler(void){
	u8 Res;
#ifdef SYSTEM_SUPPORT_OS	 	
	OSIntEnter();    
#endif
    if(USART_GetITStatus(USART1, USART_IT_RXNE) != RESET){  //接收中断(接收到的数据必须是0xFE 0xFE结尾)
        Res = USART_ReceiveData(USART1);//(USART1->DR);	//读取接收到的数据   
       
        //如果接收到的是0xFF
        if(Res == 0xFF){
            switch(transFlag){
                case TRANS_NONE:
                    usartRecieveCounter = 0;
                    transFlag = TRANS_HALFSTART;
                    break;
                case TRANS_HALFSTART:
                    transFlag = TRANS_SENDING;
                    break;
                //case TRANS_SENDING:
                    //transFlag = TRANS_READYEND;
                    //break;                                                    
            }
        //如果收到了0xFE            
        }else if(Res == 0xFE){
            switch (transFlag){
                case TRANS_SENDING:
                    transFlag = TRANS_READYEND;
                    break;
                case TRANS_READYEND:
                    transFlag = TRANS_END;
                    sendComAnalyzeEvent();
                    break;
                default:
                   //如果校验数正好是254, 也需要去掉误判.
                   transFlag = TRANS_SENDING; 
            }
                   
        }else{
            //如果收到的非0xFF0xFE, 即刻将状态机扳到发送中的状态.
            transFlag = TRANS_SENDING;
           
        }
        USART_RX_BUF[usartRecieveCounter] = Res;  
        usartRecieveCounter++;   
        
    }
#ifdef SYSTEM_SUPPORT_OS	 
	OSIntExit();  											 
#endif
}

void USART1_IRQHandler_bak(void)                	//串口1中断服务程序
{
	u8 Res;

#ifdef SYSTEM_SUPPORT_OS	 	
	OSIntEnter();    
#endif
	if(USART_GetITStatus(USART1, USART_IT_RXNE) != RESET)  //接收中断(接收到的数据必须是0x0d 0x0a结尾)
		{
		Res =USART_ReceiveData(USART1);//(USART1->DR);	//读取接收到的数据
		
		if((USART_RX_STA&0x8000)==0)//接收未完成
			{
			if(USART_RX_STA&0x4000)//接收到了0x0d
			{
				if(Res!=0x0a){
                    USART_RX_STA=0;//接收错误,重新开始
				}else{ 
                    sendComAnalyzeEvent();
                    USART_RX_STA|=0x8000;	//接收完成了 
				}
            }
			else //还没收到0X0D
			{	
                //
				if(Res==0x0d)USART_RX_STA|=0x4000;
				else
				{
					USART_RX_BUF[USART_RX_STA&0X3FFF]=Res ;
					USART_RX_STA++;
					if(USART_RX_STA>(USART_REC_LEN-1))USART_RX_STA=0;//接收数据错误,重新开始接收	  
				}		 
			}
		}   		 
     } 
#ifdef SYSTEM_SUPPORT_OS	 
	OSIntExit();  											 
#endif
} 
